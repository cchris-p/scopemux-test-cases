// JavaScript program demonstrating variables, loops, and conditions

function main() {
    // Variable declaration
    let count = 5;

    // Loop
    for (let i = 0; i < count; i++) {
        // Conditional statement
        if (i % 2 === 0) {
            console.log(i + " is even");
        } else {
            console.log(i + " is odd");
        }
    }
}

main();
